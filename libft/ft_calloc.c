#include "../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*buf;

	buf = malloc(count * size);
	if (NULL == buf)
		return (NULL);
	ft_bzero(buf, count * size);
	return (buf);
}
