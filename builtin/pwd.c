#include "../includes/builtin.h"

int	pwd(int fd)
{
	char	pwd[1025];

	g_conf.com->prev_ret = 0;
	getcwd(pwd, 1024);
	write(fd, pwd, ft_strlen(pwd));
	write(fd, "\n", 1);
	return (0);
}
